// Assignment Operators Examples

// 1. Basic Assignment (=)
var x = 10;
console.log("1. x =", x); // 10

// 2. Add and Assign (+=)
x += 5; // same as x = x + 5
console.log("2. x += 5 →", x); // 15

// 3. Subtract and Assign (-=)
x -= 3; // same as x = x - 3
console.log("3. x -= 3 →", x); // 12

// 4. Multiply and Assign (*=)
x *= 2; // same as x = x * 2
console.log("4. x *= 2 →", x); // 24

// 5. Divide and Assign (/=)
x /= 4; // same as x = x / 4
console.log("5. x /= 4 →", x); // 6

// 6. Modulus and Assign (%=)
x %= 4; // same as x = x % 4
console.log("6. x %= 4 →", x); // 2 (remainder when 6 ÷ 4)

// 7. Exponent and Assign (**=)
x **= 3; // same as x = x ** 3
console.log("7. x **= 3 →", x); // 8 (2³)
