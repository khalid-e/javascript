// JavaScript Comparison Operators Example

const a = 10;
const b = 20;
const c = "10";

console.log("a =", a, ", b =", b, ", c =", c);

// Equal to
console.log("a == c:", a == c); // true (compares value only)
console.log("a === c:", a === c); // false (compares value and type)

// Not equal to
console.log("a != b:", a != b); // true
console.log("a !== c:", a !== c); // true

// Greater than
console.log("b > a:", b > a); // true

// Less than
console.log("a < b:", a < b); // true

// Greater than or equal to
console.log("a >= 10:", a >= 10); // true

// Less than or equal to
console.log("b <= 20:", b <= 20); // true