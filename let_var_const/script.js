// Example of var
var greeting = "Hello";
console.log("var example:", greeting);

var greeting = "Hi again"; // var allows redeclaration
console.log("var redeclared:", greeting);

// Example of let
let age = 25;
console.log("let example:", age);

age = 30; // let allows reassignment
console.log("let reassigned:", age);

// let age = 35; ❌ ERROR: can't redeclare with let in same scope

// Example of const
const pi = 3.14;
console.log("const example:", pi);

// pi = 3.1415; ❌ ERROR: can't reassign a const
// const pi = 3.15; ❌ ERROR: can't redeclare a const


